// Définition de la fonction pour calculer la somme de deux nombres
function calculerSomme(a, b) {
    return a + b;
}

// Exemple d'utilisation de la fonction
let nombre1 = 5;
let nombre2 = 3;
let resultat = calculerSomme(nombre1, nombre2);

console.log(`La somme de ${nombre1} et ${nombre2} est égale à ${resultat}.`);
